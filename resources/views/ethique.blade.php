                                        <h2><a href="">Introduction</a></h2>

                                        <p>L'IRMA a pour objectif de parrainer et de soutenir les normes éthiques et professionnelles applicables à tous ses affiliés, aux détenteurs de ses titres professonnels ainsi qu'aux participants à ses formations.</p>
                                        <br>


                                        <h2><a href="">Code de Conduite</a></h2>

                                        <p>En tant qu'affilié, détenteur d'un de nos titres professonnels ou participant à ses formations, vous devez:</p>

                                        <p>
                                        </p><ul style="list-style: disc !important; margin-left: 4rem;">

                                            <li style="list-style: disc;">faire preuve d'honnêteté, d'objectivité, d'intégrité, de responsabilité, de diligence et de capacité d’être comptable de vos actes dans l'exercice de vos fonctions et responsabilités.</li>

                                            <li style="list-style: disc;">traiter ceux avec qui vous êtes en contact de manière équitable, respectueuse et sans discrimination;</li>

                                            <li style="list-style: disc;">Respecter toutes les lois pertinentes et les exigences des autorités réglementaires, codes de pratique et codes de conduite dans votre juridiction. Cela signifie pas
                                                travailler uniquement dans le cadre de la loi, mais dans l'esprit de la loi;</li>

                                            <li style="list-style: disc;">Traiter avec les régulateurs de manière ouverte et coopérative et conformément avec leurs exigences;</li>

                                            <li style="list-style: disc;">Tenir dûment compte de la santé publique, de la sécurité et de l'environnement;</li>

                                            <li style="list-style: disc;">Accorder une considération juste et appropriée et une priorité appropriée à tous les intérêts et les exigences des collègues, employés, employeurs et clients;</li>

                                            <li style="list-style: disc;">Obtenir et fournir sur demande des informations et une documentation claires pertinentes pour la demande et de respecter la confidentialité de ces les informations et la documentation sauf disposition contraire de la loi;</li>

                                            <li style="list-style: disc;">Agir en tout temps avec compétence, soin et diligence;</li>

                                            <li style="list-style: disc;">Agir uniquement dans les limites de la compétence personnelle et de toute limite de autorisation;</li>

                                            <li style="list-style: disc;">Agir avec fidélité et de manière honnête et prudente, et assurer la protection des biens, y compris de l'argent, qui vous sont confiés par les clients;</li>

                                            <li style="list-style: disc;">vous assurer que toute activité ou action, et toute publicité ou autre annonce publique auxquelles vous, votre nom ou vos qualifications sont associés ne pas discréditent pas l’Association, vos collègues, la profession des assureurs et de risk manager ainsi que votre organisation et ne contenient aucune déclaration, promesse ou recommandation frauduleuse, fausse ou trompeuse;</li>


                                            <li style="list-style: disc;">vous assurer que votre affiliation n'est pas rendue publique d'une manière qui insinue  que vous possedez une qualification professionnelle que vous ne détenez pas;</li>

                                            <li style="list-style: disc;">vous assurer, dans la mesure du possible et dans le cadre de votre autorité, que ceux qui travaillent pour vous possèdent une formation, une éducation et une supervision appropriées. Sinon, vous devriez les encourager à répondre à cette exigence.</li>

                                            <li style="list-style: disc;">éviter les conflits d'intérêts ou l'apparence de tels conflits, et ne pas divulguer toutes circonstances ou intérêts qui pourraient influencer votre jugement et votre objectivité.</li>

                                            <li style="list-style: disc;">ne pas accepter aucun objet de valeur de la part d’un employé, d’un client, d’un usager, d’un fournisseur ou d’un associé de votre employeur qui pourrait altérer ou être présumé altérer votre jugement professionnel.</li>


                                            <li style="list-style: disc;">présenter avec exactitude et honnêteté tous les faits essentiels et matériels aux décisions du client ou de votre employeurs relatives aux assurances ou au risk management du client ou de l’employeur.</li>

                                        </ul>
                                        <p></p>
                                        <br>

                                        <h2><a href=""> Code supplémentaire pour les titulaires des Titres Professionels, </a></h2>

                                        <p>Les affiliés détenteurs d'un de nos Titres Professionnels ont les responsabilités supplémentaires envers l'IRMA ainsi qu'aux professions d'assureurs et de Risk manager dans le monde entier.</p>

                                        <p>
                                        </p><ul style="list-style: disc !important; margin-left: 4rem;">

                                            <li style="list-style: disc;">Ils sont tenus de poursuivre leur développement professionnel par des moyens appropriés afin de garantir que leurs connaissances techniques ne tombe sous la norme requise.</li>

                                            <li style="list-style: disc;">Ils doivent se conformer et être en mesure de fournir la preuve du respect des éxigences du programme de Développement Professionnel Continu de l’IRMA.</li>

                                            <li style="list-style: disc;">Ils doivent s'assurer qu'IRMA est rapidement informé de tout changement dans leurs circonstances qui pourraient affecter leur droit de continuer à utiliser leurs titres.</li>

                                        </ul>
                                        <p></p>
                                        <br>


                                        <h2><a href="">Compétences et Professionalisme</a></h2>

                                        <p>Nos affiliés doivent s’efforcer, par leur initiative personnelle, de maintenir leurs compétences professionnelles et de se tenir au courant des problèmes émergents, des tendances mondiales, etc. dans le domaine de la gestion des risques.</p>
                                        <p>Il est très recommandé que nos affiliés participent de manière continue et active aux activités éducatives et professionnelles formelles ou informelles liées aux assurances et au risk management.</p>
                                        <p>Nos affiliés doivent contribuer au développement de la profession des assurances et du risk management par le partage de compétences, d'idées et de participation aux réseautages pour promouvoir et améliorer les compétences de la profession.</p>
                                        <br>

                                        <h2><a href="">Sanctions</a></h2>
                                        <p>Les normes de conduite énoncées dans ce code d'éthique fournissent des principes d’éthique de base dans la profession d’assureurs et de risk manager.</p>
                                        <p>Nos affiliés, les détenteurs de nos titres ainsi que les participants à nos formations doivent comprendre que leur jugement individuel est requis dans l'application de ces principes.</p>
                                        <p> Cependant, si, à n’importe quel moment, ils sont soupçonnés d’agir de manière contraire à l’éthique ou contre les meilleurs intérêts de l’Association ou contre les statuts ou les règlements d'IRMA, une telle conduite fera l’objet d'une enquête et pourrait entraîner des mesures disciplinaires contre eux, y compris l’éventuelle résiliation de leur affiliation et leur expulsion. </p>

